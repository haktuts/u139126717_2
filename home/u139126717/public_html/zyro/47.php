<p> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10631;">•&gt;<strong>Algorithm</strong></span> <span style="color:#454545;">- A series of steps specifying which actions to take in which order.</span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="background-color: transparent;">•<span style="color:#f10631;">&gt;<strong>ANSI Bomb</strong></span> <span style="color:#454545;">- ANSI.SYS key-remapping commands consist of cryptic-looking text that specifies, using ansi numeric codes to redefine keys.</span></span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="background-color: transparent;"><span style="color:#f10631;">•&gt;<strong>Back Door</strong></span> <span style="color:#454545;">- Something a hacker leaves behind on a system in order to be able to get back in at a later time.</span></span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="background-color: transparent;"><span style="color:#f10631;">•&gt;<strong>Binary</strong></span> <span style="color:#454545;">- A numbering system in which there are only two possible values for each digit: 0 and 1.</span></span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="background-color: transparent;"><span style="color:#f10631;">•<strong>Black Hat</strong></span> <span style="color:#454545;">- A hacker who performs illegal actions to do with hacking online. (Bad guy, per se)</span></span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="background-color: transparent;"><span style="color:#f10631;">•&gt;<strong>Blue Hat</strong></span> <span style="color:#454545;">- A blue hat hacker is someone outside computer security consulting firms who is used to bug test a system prior to its launch, looking for exploits so they can be closed. Microsoft also uses the term BlueHat to represent a series of security briefing events.</span></span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="background-color: transparent;"><span style="color:#f10631;">•&gt;<strong>Bot</strong> </span><span style="color:#454545;">- A piece of malware that connects computer to an attacker commonly using the HTTP or IRC protocal to await malicous instructions.</span></span></span></p>

