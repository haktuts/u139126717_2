<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="background-color: transparent;"><span style="color:#f10631;">•&gt;<strong>Cracker</strong> </span><span style="color:#454545;">- A specific type of hacker who decrypts passwords or breaks software copy protection schemes.</span></span></span></p>

