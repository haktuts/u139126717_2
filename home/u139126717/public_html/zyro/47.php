<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="background-color: transparent;"><span style="color:#f10631;">•&gt;<strong>Botnet</strong></span> <span style="color:#454545;">- Computers infected by worms or Trojans and taken over by hackers and brought into networks to send spam, more viruses, or launch denial of service attacks.</span></span></span></p>

<p class="wb-stl-normal"> </p>

