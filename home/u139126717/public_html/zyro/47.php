<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10631;">•&gt;<strong>Deface</strong><span style="color:#454545;"> </span></span><span style="color:#454545;">- A website deface is an attack on a site that changes the appearance of the site or a certain webpage on the site.</span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="background-color: transparent;"><span style="color:#f10631;">•&gt;<strong>Dictionary Attack </strong></span><span style="color:#454545;">- A dictionary attack is an attack in which a cyber criminal can attempt to gain your account password. The attack uses a dictionary file, a simple list of possible passwords, and a program which fills them in. The program just fills in every single possible password on the list, untill it has found the correct one. Dictionary files usually contain the most common used passwords.</span></span></span></p>

