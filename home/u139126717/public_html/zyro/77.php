<h1 class="wb-stl-heading1"><span style="color:#f10930;">iOS Jailbreak</span></h1>

<p> </p>

<p class="wb-stl-normal"><br><span style="color:#454545;">iOS jailbreaking is the process of removing limitations on iOS, Apple's operating system on devices running it through the use of software and hardware exploits; such devices include the IPhone, IPod touch, IPad, and second-generation Apple TV. Jailbreaking permits root access to the iOS file system and manager, allowing the download of additional applications, extensions, and themes that are unavailable through the official Apple App Store.<br>
 <br><br>
Jailbreaking is a form of privilege escalation,and the term has been used to describe privilege escalation on devices by other manufacturers as well. The name refers to breaking the device out of its "jail", which is a technical term used in Unix-style systems, for example in the term "FreeBSD jail". A jailbroken IPhone, IPod touch, or IPad running iOS can still use the App Store, iTunes, and other normal functions, such as making telephone calls.<br>
Restoring a device with iTunes removes the jailbreak.</span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"> </p>

<h2 class="wb-stl-heading2"><span style="color:#454545;">Is Jailbreaking the Same as Unlocking?</span></h2>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#454545;">Jailbreaking is different to IPhone unlocking. IPhones (like all mobile phones) that are sold as part of a contract are often locked to a particular network. This means that if you buy an IPhone from AT&amp;T, for example, you have to use an AT&amp;T SIM card in it. The IPhone is ‘locked’ to that network. Unlocking the IPhone turns it from an IPhone that can only work on the AT&amp;T network to one that can work on any network. But you will still be running officially sanctioned iOS software, and still have the software limitations. Unlocking is considered a breach of your mobile phone contract.</span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#454545;">Jailbreak Security While the upside of jailbreaking is more control over what is installed on a device, and that it sometimes allows users to do things Apple doesn't approve of (such as, creating ringtones before the feature was available in iTunes), it can also cause problems, including data corruption and allowing hackers and criminals to install malicious code on jailbroken devices.</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">Apple does not support devices that have been damaged due to jailbreaking and may consider them out of warranty.</span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"> </p>

