<p class="wb-stl-normal"><span style="color:#454545;">       Cydia is like appstore ,you can browse Cydia for apps, tweaks, mods.</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">       You can download and install Cydia apps just like the way you download app from apple app store.</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">       Most of the apps/tweaks in Cydia are free, but it is not unsual for a jailbreak app to sell for a few dollars.</span></p>

