<h3 class="wb-stl-heading3"><span style="color:#454545;">Can I Still Use iTunes and the App Store After Jailbreaking?</span></h3>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#454545;">       Yes, you can use iTunes and the App Store after jailbreaking your device. As a matter of fact, nothing will really change. The only notable change to your iOS device will be that, after jailbreaking, you will have a new application installed on your device called Cydia.</span></p>

<p class="wb-stl-normal"> </p>

