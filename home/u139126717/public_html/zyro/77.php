<p class="wb-stl-normal"><span style="color:#f10938;">Different Types of Jailbreaks:</span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10938;">Tethered Jailbreak:</span> <span style="color:#454545;">Tethered jailbreaks require you to plug your iDevice into the computer to start it up because the device needs some code from a program on the computer that will let it boot up. The reason it needs this code is because the device checks for unsigned software running and it will not let it boot up without the code from the computer.</span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10938;">Untethered Jailbreak:</span> <span style="color:#454545;">An untethered jailbreak doesn't require you to plug you iDevice into the computer to boot because the jailbreak patches the file that checks for unsigned software.</span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10938;">Semi-Tethered:</span> <span style="color:#454545;">A semi-tethered jailbreak allows you to start your iDevice without plugging it into your computer however you will not be able to use your jailbroken addons until you boot from your computer using a program such as redsn0w.</span></span></p>

