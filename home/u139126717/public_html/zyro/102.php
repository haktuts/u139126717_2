<p class="wb-stl-normal"><span style="color:#454545;">RecordMyScreen tweak provides an instant way for iOS users to record their on screen activity and share it with their friends. After installing this tweak users can assign an Activator action to it and instantly start recording after performing that action. There are a few options to configure that can be found within the Settings app. RecordMyScreen is available for free.</span></p>

