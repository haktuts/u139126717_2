<p> </p>

<p class="wb-stl-normal">Mozilla, the developer of the internet browser Firefox, has teamed up with Center for Democracy &amp; Technology (CDT) and the Tor Project (The Onion Router) to provide internet users with greater privacy through their new initiative—Polaris, according to the company’s privacy blog.</p>

<p class="wb-stl-normal">Polaris, launched two days ago, is focused on anti-censorship technology, anonymity and cross-site tracking protection.</p>

<p class="wb-stl-normal">It is “designed to allow us to collaborate more effectively, more explicitly and more directly to bring more privacy features into our products,” says the Mozilla website.</p>

<p class="wb-stl-normal">It will have many privacy features including the company’s already introduced features such as Do Not Track preference; Private and Guest Browsing; high levels of encryption with Firefox Sync; an individual approach to apps permissions; and a Forget button.</p>

<p class="wb-stl-normal">The company is currently evaluating the Tor Project’s changes to Firefox’s codebase to see effectiveness of Tor, which works by bouncing its way randomly around servers manned by volunteers globally. This feature makes it difficult for the surveillance companies to track a user’s activities online. However, its only drawback being its speed which can be pretty annoying if the connection is less than ideal. The two companies plan to integrate best features of both—Mozilla’s speed and Tor’s security.</p>

<p class="wb-stl-normal">It is also working towards providing a feature that can protect its users from invasive tracking without penalizing the advertisers and content sites that respect user’s preferences.</p>

<p class="wb-stl-normal">It has taken a cue from the 2014 Harris Poll that concluded that people are concerned about the reduced privacy of their personal information on the web.</p>

<p class="wb-stl-normal">The company also wants to change the perception of internet privacy as something too complex for a layman to understand.</p>

<p class="wb-stl-normal">It said on its website,<br>
“We want to accelerate pragmatic and user-focused advances in privacy technology for the Web, giving users more control, awareness and protection in their Web experiences. We want to advance the state of the art in privacy features, with a specific focus on bringing them to more mainstream audiences.”</p>

<p class="wb-stl-normal">Its collaborators are also excited about the new initiative.</p>

<p class="wb-stl-normal">“CDT looks forward to working with Mozilla on the Polaris program and advising on issues like combating Internet censorship and protecting online anonymity, which are vital to promoting free expression online,” said Justin Brookman of CDT.</p>

<p class="wb-stl-normal">Andrew Lewman of the Tor Project said:</p>

<p class="wb-stl-normal">“The Tor Project is excited to join Mozilla as a launch partner in the Polaris program. We look forward to working together on privacy technology, open standards, and future product collaborations.”</p>

<p class="wb-stl-normal">Mozilla is the forerunner internet company seeking transparency from the US government since Snowden’s revelations about alleged snooping surfaced.</p>

