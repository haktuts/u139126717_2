<p class="wb-stl-normal"><br><span style="color:#bebebe;"><span style="color:#f10631;">1.IPv4</span><span style="color:#454545;">:Internet Protocol version 4 is the fourth version in the development of the Internet Protocol Internet, and routes most traffic on the </span></span><span style="color:#454545;">Internet. Addresses are 32 bits (8 bytes) in length. Mobile phone do not support IPv4. Number of Addresses 2^32 = ~4,294,967,296</span></p>

<p class="wb-stl-normal"><br><span style="color:#bebebe;"><span style="color:#f10631;">2.IPv6:</span> <span style="color:#454545;"><strong>Internet Protocol version 6 (IPv6)</strong> is the latest version of the Internet Protocol (IP), the communications protocol that provides an identification and location system for computers on networks and routes traffic across the Internet.</span></span></p>

<p class="wb-stl-normal"><span style="color:#454545;">IPv6 was developed by the Internet Engineering TaskForce   (IETF) to deal with the long-anticipated problem of IPv4 address exhaustion. Addresses are 128 bits (16 bytes) inlength.</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">Hexadecimal Notation:3FFE:F200:0234:AB00:0123:4567:8901:ABCD. Number of Addresses2^128 = ~340,282,366,920,938,463,463,374,607,431,768,211,456</span></p>

<p class="wb-stl-normal"><br><br><span style="color:#bebebe;"><span style="color:#f10631;">3.Public  IP<span style="color:#454545;">:</span></span><span style="color:#454545;"> Public IP Addresses (also known as static IP Addresses) are IP Addresses that are visible to the public. Because they are public, they allow other people to know about and access your computer, like a Web Server. Public IP based on class. For different class different IP allocated  as given below:</span></span></p>

<p class="wb-stl-normal"><span style="color:#454545;">        Class A : 1-126</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">        Class B: 128-191</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">        Class C : 192-223</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">        Class D : 224-239</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">        Class E : 240-254</span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10631;">4.   Private IP :</span> <span style="color:#454545;">The Internet Assigned Numbers Authority (IANA) has reserved the following three blocks of the IP address space for private internets (local networks).</span></span></p>

<p class="wb-stl-normal"><span style="color:#454545;">        Class A : 10.0.0.0</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">        Class B : 172.16.0.0 - 172.31.0.0</span></p>

<p class="wb-stl-normal"><span style="color:#454545;">        Class C : 192.168.0.0</span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10631;">5.   Static IP :</span><span style="color:#454545;"> A static IP address is an IP address that was manually configured for a device, verses one that was assigned via a DHCP server. A static IP address is called static because it does not change.</span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10631;">6.   Dynamic IP :</span><span style="color:#454545;"> A dynamic IP address is an IP address assigned by a DHCP server, verses one that was configured manually.  DHCP server assigned IP address is called dynamic because it will often be different on each connection to the network.</span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10631;">7.   Internal IP address :</span><span style="color:#454545;"> Internal IP Addresses (also known as static IP Addresses) are IP Addresses that are not visible to the public. Because they are private, they do not allow other people to know about and they cannot access your computer, like a Web Server</span></span></p>

<p class="wb-stl-normal"> </p>

<p class="wb-stl-normal"><span style="color:#bebebe;"><span style="color:#f10631;">8.   External IP address :</span> <span style="color:#454545;">External IP Addresses (also known as Dynamic IP Addresses) are IP Addresses that are visible to the public. Because they are public, they allow other people to know about and access your computer, like a Web Serve</span></span></p>

