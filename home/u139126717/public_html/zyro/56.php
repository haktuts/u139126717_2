<h3 class="wb-stl-heading3"><span style="color:#454545;">Proxy</span></h3>

<p> </p>

<p> </p>

<p><span style="color:#454545;"><strong>1.</strong> A proxy server is a computer on the network between you and the other computers on the Internet. usually, what happens when you connect to a site, your computer sends a request to the server you're trying to receive data from but when you connect to a proxy, you send the request to the proxy, and the proxy sends the data back to you.</span></p>

<p> </p>

<p><span style="color:#454545;"><strong>2.</strong> Proxy divided into three types</span></p>

<p><span style="color:#454545;">          </span></p>

<p><span style="color:#454545;">          Normal proxy:</span></p>

<p><span style="color:#454545;">          Transparent proxy</span></p>

<p><span style="color:#454545;">           Reverse proxy</span></p>

<p> </p>

<p> </p>

<p><span style="color:#454545;">Normal Proxy</span></p>

<p> </p>

<p><span style="color:#454545;">1.Benefit for client.</span></p>

<p><br><span style="color:#454545;">2.Client side verification required</span></p>

<p><br><span style="color:#454545;">3.Proxy working in browser.</span></p>

<p><br><span style="color:#454545;">4.It stores a copy for future use. So next time when another client requests for the same webpage the proxy server just replies to the request with the content in its cache thus improving the overall request-reply speed.</span></p>

<p> </p>

<p> </p>

<p>Transparent Proxy</p>

<p> </p>

<p><span style="color:#454545;">1.Benefit for client.</span></p>

<p><br><span style="color:#454545;">2.Client side configuration not required</span></p>

<p><br><span style="color:#454545;">3.Proxy working in Gateway like port forwarding.</span></p>

<p><br><span style="color:#454545;">4.It fetches the content for the first time and subsequently replies from its local cache.Eg Torrent</span></p>

<p> </p>

<p> </p>

<p><span style="color:#454545;">Reverse Proxy</span></p>

<p> </p>

<p><span style="color:#454545;">1.Benefit for web server</span></p>

<p><br><span style="color:#454545;">2.Proxy working in web server </span></p>

<p><br><span style="color:#454545;">3.It will cache all the static answers from the web server and reply to the clients from its cache to reduce the load on the web server.</span></p>

<p><br><span style="color:#454545;">4.This type of setup is also known as Web Server Acceleration</span></p>

